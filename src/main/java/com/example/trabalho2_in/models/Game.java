package com.example.trabalho2_in.models;

public class Game {
    private String name;
    private String platform;
    private Integer year;
    private Double globalSales;
    private Double sports;
    private Double racing;
    private Double rolePlaying;
    private Double puzzle;
    private Double misc;
    private Double shooter;
    private Double simulation;
    private Double action;
    private Double fighting;
    private Double adventure;
    private Double strategy;
    private String cluster;

    public Game() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPlatform() {
        return platform;
    }

    public void setPlatform(String platform) {
        this.platform = platform;
    }

    public Integer getYear() {
        return year;
    }

    public void setYear(Integer year) {
        this.year = year;
    }

    public Double getGlobalSales() {
        return globalSales;
    }

    public void setGlobalSales(Double globalSales) {
        this.globalSales = globalSales;
    }

    public Double getSports() {
        return sports;
    }

    public void setSports(Double sports) {
        this.sports = sports;
    }

    public Double getRacing() {
        return racing;
    }

    public void setRacing(Double racing) {
        this.racing = racing;
    }

    public Double getRolePlaying() {
        return rolePlaying;
    }

    public void setRolePlaying(Double rolePlaying) {
        this.rolePlaying = rolePlaying;
    }

    public Double getPuzzle() {
        return puzzle;
    }

    public void setPuzzle(Double puzzle) {
        this.puzzle = puzzle;
    }

    public Double getMisc() {
        return misc;
    }

    public void setMisc(Double misc) {
        this.misc = misc;
    }

    public Double getShooter() {
        return shooter;
    }

    public void setShooter(Double shooter) {
        this.shooter = shooter;
    }

    public Double getSimulation() {
        return simulation;
    }

    public void setSimulation(Double simulation) {
        this.simulation = simulation;
    }

    public Double getAction() {
        return action;
    }

    public void setAction(Double action) {
        this.action = action;
    }

    public Double getFighting() {
        return fighting;
    }

    public void setFighting(Double fighting) {
        this.fighting = fighting;
    }

    public Double getAdventure() {
        return adventure;
    }

    public void setAdventure(Double adventure) {
        this.adventure = adventure;
    }

    public Double getStrategy() {
        return strategy;
    }

    public void setStrategy(Double strategy) {
        this.strategy = strategy;
    }

    public String getCluster() {
        return cluster;
    }

    public void setCluster(String cluster) {
        this.cluster = cluster;
    }
}
